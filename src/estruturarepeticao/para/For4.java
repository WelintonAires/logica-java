package estruturarepeticao.para;

public class For4 {
    public static void main(String[] args) {
        int x = 4;
        int y = 0;
        int i = 0;
        for (i = 0; i < x; i++) {
            System.out.print(i);
            System.out.println(x);
            y = y + 10;
        }
    }
}
